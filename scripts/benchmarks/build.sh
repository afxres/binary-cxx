#!/bin/bash

PROJECT_ROOT="$PWD"
PROJECT_DIR="$PROJECT_ROOT/next"
BUILD_DIR="$PROJECT_ROOT/build-benchmarks"
DEPENDENCIES_DIR="$PROJECT_ROOT/dependencies"
GOOGLE_BENCHMARK_DIR="$DEPENDENCIES_DIR/benchmark"
GOOGLE_BENCHMARK_BUILD_DIR="$GOOGLE_BENCHMARK_DIR/build"
export benchmark_DIR="$GOOGLE_BENCHMARK_BUILD_DIR"

mkdir -p "$BUILD_DIR"
cd "$BUILD_DIR"
cmake -DCMAKE_BUILD_TYPE=Release -DBINARY_BUILD_BENCHMARKS=ON "$PROJECT_DIR"
cmake --build . --parallel
